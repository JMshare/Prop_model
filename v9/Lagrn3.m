function [y] = Lagrn3(x, p1, p2, p3, p4)

x1 = p1(1);
y1 = p1(2);
x2 = p2(1);
y2 = p2(2);
x3 = p3(1);
y3 = p3(2);
x4 = p4(1);
y4 = p4(2);

y = (y1*(x-x2)*(x-x3)*(x-x4))/((x1-x2)*(x1-x3)*(x1-x4)) +...
    (y2*(x-x1)*(x-x3)*(x-x4))/((x2-x1)*(x2-x3)*(x2-x4)) +...
    (y3*(x-x1)*(x-x2)*(x-x4))/((x3-x1)*(x3-x2)*(x3-x4)) +...
    (y4*(x-x1)*(x-x2)*(x-x3))/((x4-x1)*(x4-x2)*(x4-x3));

end